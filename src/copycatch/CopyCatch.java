    private void JB1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB1ActionPerformed
        // TODO add your handling code here:
        JTA1.setText(" ");
        commons.clear();
        hmf.clear();
        JB2.setEnabled(false);
        JB3.setEnabled(false);
        JB4.setEnabled(false);
        JL3.setText("Messages will appear here");
        f1name=JT1.getText();
        f2name=JT2.getText();
        if(f1name.equals(f2name))
        {
            JTA1.setText("Both are same Folders");
            return;
        }
        f1 = new File(f1name);
        f2 = new File(f2name);
        if (!f1.exists())
        {
            JTA1.setText("Folder ONE does not exist");
            return;
        }
        if (!f2.exists())
        {
            JTA1.setText("Folder TWO does not exist");
            return;
        }
        
        File[] lof1 = f1.listFiles();
        for(int i=0;i<lof1.length;i++)
        {
            if(lof1[i].isFile())
            {
                hmf.put(lof1[i].getName(),0);
            }
        }
        
        File[] lof2 = f2.listFiles();
        for(int i=0;i<lof2.length;i++)
        {
            if(hmf.containsKey(lof2[i].getName()))
            {
                hmf.put(lof2[i].getName(),1);
            }
        }
        
        for(Map.Entry e : hmf.entrySet())
        {
            if((int)e.getValue()==1)
            {
                commons.add((String)e.getKey());
            }
        }

        if(commons.isEmpty())
        {
            JTA1.setText("No common Files in both the Folders");
            return;
        }
        
        JTA1.setText("Common Files are-\n");
        
        for(int i=0;i<commons.size();i++)
	{
            JTA1.append(commons.get(i)+"\n");
	}
        JB2.setEnabled(true);
        JB3.setEnabled(true);
        JB4.setEnabled(true);
        delf1= false;
        delf2= false;
    }//GEN-LAST:event_JB1ActionPerformed

    private void JB2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB2ActionPerformed
        // TODO add your handling code here:
        if(delf1==true)
        {
            JL3.setText("All Common Files already deleted from Folder 1");
            JB2.setEnabled(false);
            return;
            
        }
        delf1 = true;
        for(int i=0;i<commons.size();i++)
        {
            File f = new File(f1name+"/"+commons.get(i));
            f.delete();
	}
	JL3.setText("All common Files deleted from folder 1");
        JB2.setEnabled(false);
        if(delf1==true && delf2==true)
        {
            JB4.setEnabled(false);
            JL3.setText("All common files deleted from both the folders");
        }
    }//GEN-LAST:event_JB2ActionPerformed

