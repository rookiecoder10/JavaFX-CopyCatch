    private void JB5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB5ActionPerformed
        // TODO add your handling code here:
        String f3name = JT3.getText();
        File f = new File(f3name);
        if(!f.exists())
        {
            boolean status=new File(f3name).mkdir();
            if(status==false)
            {
                JL3.setText("Unable to create new folder in specified path");
                return;
            }
				 
        }
        try
        {
            if(delf1==false)
            {
                for(int i=0;i<commons.size();i++)
                {
                    f = new File(f3name+"/"+commons.get(i));
                    f.createNewFile();
                    FileOutputStream os = new FileOutputStream(f3name+"/"+commons.get(i));
                    FileInputStream is = new FileInputStream(f1name+"/"+commons.get(i));
                    int c;
                    do
                    {
                        c=is.read();
                        if(c!=-1)
                        {
                            os.write((char)c);
                        }
                    }while(c!=-1);
                    os.close();
                    is.close();
                }
            }
            else if(delf2==false)
            {
                for(int i=0;i<commons.size();i++)
                {
                    f = new File(f3name+"/"+commons.get(i));
                    f.createNewFile();
                    FileOutputStream os = new FileOutputStream(f3name+"/"+commons.get(i));
                    FileInputStream is = new FileInputStream(f2name+"/"+commons.get(i));
                    int c;
                    do
                    {
                        c=is.read();
                        if(c!=-1)
                        {
                            os.write((char)c);
                        }
                    }while(c!=-1);
                    os.close();
                    is.close();
                }
            }
        }
        catch(Exception e)
        {
            JL3.setText("Unknown error ouccurd, please contact support- "+e.getMessage());
        }
        JL3.setText("Common Files successfully copied to foleder "+f3name);
        if(delf1==false)
            JB2.setEnabled(true);
        if(delf2==false)
            JB3.setEnabled(true);
        JT3.setEditable(false);
        JT3.setEnabled(false);
        JB5.setEnabled(false);
    }//GEN-LAST:event_JB5ActionPerformed

